package ejercicio3;

public class Cliente {
    
    private String DNI;
    private String nombre;
    private String apellido;
    private String ciudadOrigen;
    private double saldo;

    public Cliente(String DNI, String nombre, String apellido, String ciudadOrigen, double saldo) {
        this.DNI = DNI;
        this.nombre = nombre;
        this.apellido = apellido;
        this.ciudadOrigen = ciudadOrigen;
        this.saldo = saldo;
    }

    
}
